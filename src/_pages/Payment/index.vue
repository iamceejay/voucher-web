<template>
  <MainLayout>
    <template #content>
      <div v-if="!IS_LOADING.status" class="content-container flex flex-col w-full px-8">
        <Header1
          label="Zahlungsmethode"
        />
        <div class="flex flex-col w-full md:w-1/2">
          <div class="flex flex-col mt-5 w-full">
            <span class="text-lg font-bold text-center">
              Price
            </span>
            <span class="text-lg font-bold text-center">
              {{ $helpers.convertCurrency(totalPrice) }}
            </span>
          </div>
          <div v-if="is_save" class="flex flex-col">
            <Button
              variant="info"
              class="py-2 justify-center"
              label="Neue Kreditkarte"
              size="w-full py-3"
              round="rounded-full"
              fontSize="text-sm"
              @onClick="is_save = false"
            /> 
            <Button
              class="py-2 justify-center"
              label="Jetzt bezahlen"
              size="w-full py-3"
              round="rounded-full"
              fontSize="text-sm"
              @onClick="onSubmit"
            />   
          </div>
          <StripeForm 
            v-else
            @onSubmit="onSubmit"
          />
        </div>
      </div>
    </template>
  </MainLayout>
</template>
<script>
  import MainLayout from '_layouts';
  import Header1 from '_components/Headers/Header1';
  import StripeForm from '_components/Form/Modules/StripeForm';
  import Button from '_components/Button'

  export default {
    components: {
      MainLayout,
      Header1,
      StripeForm,
      Button,
    },
    data() {
      return {
        totalPrice: 0,
        is_save: false,
      };
    },
    computed: {
      AUTH_USER() {
        return this.$store.getters.AUTH_USER;
      },
      USER() {
        return this.$store.getters.USER;
      },
      WALLETS()
      {
        return this.$store.getters.WALLETS
      },
      IS_LOADING()
      {
        return this.$store.getters.IS_LOADING
      },
    },
    watch: {
      async WALLETS(newVal)
      {
        this.totalPrice = 0
        await this.onGetTotalPrice()
      }
    },
    mounted() {
      (async() => {
        try {
          await this.$store.commit('SET_IS_LOADING', { status: 'open' })
          await this.$store.commit('SET_WALLETS', [])
          await this.onFetchWallets()
          await this.onFetchUser()
          if( this.USER?.stripe?.is_save ) {
            this.is_save =  true
          }
          await this.onGetTotalPrice()
          await this.$store.commit('SET_IS_LOADING', { status: 'close' })
        } catch (error) {
          await this.$store.commit('SET_IS_LOADING', { status: 'close' })
        }
      })()
    },
    methods: {
      onSubmit( data )
      {
        this.$swal({
          title: 'Zahlungsmethoden',
          text: `Bist du sicher, dass du diese Zahlung durchführen möchtest?`,
          showCancelButton: true,
          confirmButtonColor: '#6C757D',
          cancelButtonColor: '#AF0000',
          confirmButtonText: 'Bestätigen',
          cancelButtonText: 'Abbrechen',
        }).then(async (result) => {
          if(result.value){
            await this.$store.commit('SET_IS_PROCESSING', { status: 'open' })
            await this.$store.dispatch('PAYMENT', {
              is_save: this.is_save,
              ...data,
              price: this.totalPrice
            })
            await this.$store.commit('SET_IS_PROCESSING', { status: 'close' })
            this.$swal({
              icon: 'success',
              title: 'Erfolgreich!',
              text: 'Paying the vouchers.',
              showCancelButton: false,
              confirmButtonColor: '#6C757D',
              confirmButtonText: 'Bestätigen',
            }).then(async (result) => {
              await this.$store.commit('SET_IS_PROCESSING', { status: 'close' })
              if(result.value){
                await this.$store.commit('SET_COUNT_CART', 0)
                this.$router.push('/wallet')
              }
            })
          }   
        })
      },
      onGetTotalPrice()
      {
        this.totalPrice = 0
        this.WALLETS.map( row => {
          this.totalPrice += this.onGetTotal(row)
        })
      },
      onGetTotal(data)
      {
        let value = (data.voucher.type == 'quantity') ? data.qty : data.value
        let total = value

        if( data.voucher.type == 'quantity' ) {
          total = value * data.voucher.price_filter
        }
        
        return total
      },
      async onFetchUser()
      {
        try {
          const data = await this.$store.dispatch('FETCH_USER', {
            id: this.AUTH_USER.data.id,
          })
        } catch (err) {
          console.log('err', err)
        }
      },
      async onFetchWallets()
      {
        await this.$store.dispatch('FETCH_WALLETS', {
          user_id: this.AUTH_USER.data.id,
          status: 'pending'
        })
      },
    }
  }
</script>
